import Script

class Actuator:
    """Defines a physical actuator of the system"""

    def __init__(self, name: str, pin: int):
        self.name = name
        self.pin = pin

    def getName():
        return self.name

    def getPin():
        return self.pin

    def getDuty_cycle():
        return self.duty_cycle

    def getActivate():
        return self.activate

    def getDeactivate():
        return self.deactivate